<script lang="ts" setup>
import {ref} from 'vue'
import {NPSConfiguration} from "easy-form-component";

const titOption = ref({
  title: '您向朋友或同事推荐我们的可能性有多大？',
  required: false,
  error: ''
})
const error = ref('')

function setError(val: string) {
  if (val.length > 10) {
    error.value = '最多输入10个字符'
  } else {
    error.value = ''
  }
}
</script>

